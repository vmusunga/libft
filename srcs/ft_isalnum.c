int     ft_isalnum(char *str)
{
    int i;

    i = 0;
    while (str[i])
    {
        if (!((str[i] <= 'z' && str[i] >= 'a')
                            || (str[i] <= '9' && str[i] >= '0')
                            || (str[i] <= 'Z' && str[i] >= 'A')))
            return (0);
        i++;
    }
    return (1);
}